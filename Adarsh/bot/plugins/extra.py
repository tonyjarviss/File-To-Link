@StreamBot.on_message(filters.regex("ping"))
async def ping(b, m):
    start_t = time.time()
    ag = await m.reply_text("....")
    end_t = time.time()
    time_taken_s = (end_t - start_t) * 1000
    await ag.edit(f"Ping!\n{time_taken_s:.3f} ms")
    
    
    
    
